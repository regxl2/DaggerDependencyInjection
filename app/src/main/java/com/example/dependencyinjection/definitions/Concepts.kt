package com.example.dependencyinjection.definitions

// Concepts

//1. Consumers:- They are the classes which consumes the classes (dependency)
//   annotations used for the consumers classes are (@Inject)

//2. Producers:- They are the classes which produces the classes (dependency)
//   annotations used for the producers classes are (@Module, @Provides, @Binds)

//3. Connectors:- They are the classes which connects the producers and consumers
//   annotations used for the connector classes are (@Component)